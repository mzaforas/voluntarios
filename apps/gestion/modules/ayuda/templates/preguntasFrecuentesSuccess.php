<li><b>Tengo problemas para descargar los documentos de un voluntario</b></li>
<p><i>La descarga de documentos y en general toda la aplicación está desarrollada y probada para funcionar sobre los navegadores Mozilla Firefox o Google Chrome, prueba a usar la aplicación con uno de estos dos programas y si sigues teniendo problemas entonces contacta con los desarrolladores</i></p>

<li><b>¿Cómo deben ser las fotografías de los voluntarios?</b></li>
<p><i>Una fotografía, tipo y formato carnet-pasaporte, en color con fondo blanco con la cabeza totalmente descubierta, sin gafas de cristales oscuros o cualquier prenda que pueda impedir la identificación de la persona. (Formato de la fotografía JPG y resolución 250 x 300 píxeles)</i></p>

